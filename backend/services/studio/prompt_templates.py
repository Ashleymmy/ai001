# ---------------------------------------------------------------------------
# 帧类型映射
# ---------------------------------------------------------------------------
FRAME_TYPE_ZH = {
    "start_frame": "首帧（动作前静态画面）",
    "key_frame": "关键帧（动作高潮瞬间）",
    "end_frame": "尾帧（动作后最终状态）",
}


def get_frame_system_prompt(stage: str) -> str:
    """根据帧类型返回对应的系统提示词模板。"""
    if stage == "key_frame":
        return KEY_FRAME_SYSTEM_PROMPT
    if stage == "end_frame":
        return END_FRAME_SYSTEM_PROMPT
    return START_FRAME_SYSTEM_PROMPT


def get_frame_type_label(stage: str) -> str:
    """获取帧类型的中文标签。"""
    return FRAME_TYPE_ZH.get(stage, "首帧（动作前静态画面）")
